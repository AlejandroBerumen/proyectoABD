    public String generarConsultaCampos(){
        String consulta = "";
                if(!jTextField1.getText().equals(""))
                    consulta = consulta + "nombre like '%"+jTextField1.getText()+"%'";
                if(!jTextField2.getText().equals("")){
                    if(!consulta.equals(""))
                        consulta = consulta + " AND ";
                    consulta = consulta + "apellidop like '%"+jTextField2.getText()+"%'";
                }
                if(!jTextField3.getText().equals("")){
                    if(!consulta.equals(""))
                        consulta = consulta + " AND ";
                    consulta = consulta + "apellidom like '%"+jTextField3.getText()+"%'";
                }
                if(!jTextField4.getText().equals("")){
                    if(!consulta.equals(""))
                        consulta = consulta + " AND ";
                    consulta = consulta + "dni like '%"+jTextField4.getText()+"%'";
                }
                if(!jTextField5.getText().equals("")){
                    if(!consulta.equals(""))
                        consulta = consulta + " AND ";
                    consulta = consulta + "direccion like '%"+jTextField5.getText()+"%'";
                }
                if(!jTextField6.getText().equals("")){
                    if(!consulta.equals(""))
                        consulta = consulta + " AND ";
                    consulta = consulta + " CAST(sueldo as text) like '%"+jTextField6.getText()+"%'";
                }
                if(!jTextField7.getText().equals("")){
                    if(!consulta.equals(""))
                        consulta = consulta + " AND ";
                    consulta = consulta + "superdni like '%"+jTextField7.getText()+"%'";
                }
                if(!jTextField8.getText().equals("")){
                    if(!consulta.equals(""))
                        consulta = consulta + " AND ";
                    consulta = consulta + "CAST(dno as text) like '%"+jTextField8.getText()+"%'";
                }
                if(consulta.equals(""))
                    consulta = "nombre is not null";
                return consulta;
    }
    
